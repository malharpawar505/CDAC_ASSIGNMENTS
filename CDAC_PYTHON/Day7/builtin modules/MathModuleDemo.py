
#Square Root (sqrt):
import math
x = 16
y = math.sqrt(x)
print(y)  # Output: 4.0

#--------------------------------------------

#Exponentiation (pow):
import math
base = 2
exponent = 3
result = math.pow(base, exponent)
print(result)  # Output: 8.0

#--------------------------------------------


#Trigonometric Functions (sin, cos, tan):
import math
angle = math.radians(30)  # Convert degrees to radians
sin_value = math.sin(angle)
cos_value = math.cos(angle)
tan_value = math.tan(angle)
print(sin_value, cos_value, tan_value) #0.49999999999999994 0.8660254037844387 0.5773502691896257

#--------------------------------------------


#Logarithmic Functions (log, log10):
import math
x = 10
natural_log = math.log(x)
base_10_log = math.log10(x)
print(natural_log, base_10_log)#2.302585092994046 1.0

#--------------------------------------------

#Ceiling and Floor (ceil, floor):
import math
number = 3.6
ceil_value = math.ceil(number)  # Rounds up to the nearest integer
floor_value = math.floor(number)  # Rounds down to the nearest integer
print(ceil_value, floor_value) #4 3


#--------------------------------------------
#Absolute Value (fabs):
    
import math
x = -5
absolute_value = math.fabs(x)
print(absolute_value)  # Output: 5.0
